# в”Җв”Җ Load checker module в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
SCRIPT_DIR = Path(__file__).parent
spec = importlib.util.spec_from_file_location(
    "check_ascii_boxes",
    SCRIPT_DIR / "check-ascii-boxes.py"
)
checker = importlib.util.module_from_spec(spec)
spec.loader.exec_module(checker)

# в”Җв”Җ Verbosity в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
VERBOSE = 0


def check(diagram_str):
    """Run the checker on a diagram string wrapped in a fenced code block.
    Returns list of issue strings (empty = pass)."""
    md = f"```\n{diagram_str}\n```\n"
    lines = md.split('\n')
    blocks = checker.extract_code_blocks(lines)
    all_issues = []
    for _start, block_lines in blocks:
        all_issues.extend(checker.check_code_block(block_lines))
    return all_issues


# в”Җв”Җ Test infrastructure в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ
_results = {'pass': 0, 'fail': 0, 'tests': []}


def expect_clean(name, diagram):
    """Assert the checker finds NO issues."""
    issues = check(diagram)
    ok = len(issues) == 0
    _results['tests'].append((name, ok, diagram, issues))
    if ok:
        _results['pass'] += 1
    else:
        _results['fail'] += 1


def expect_issues(name, diagram, min_issues=1):
    """Assert the checker finds at least min_issues problems."""
    issues = check(diagram)
    ok = len(issues) >= min_issues
    _results['tests'].append((name, ok, diagram, issues))
    if ok:
        _results['pass'] += 1
    else:
        _results['fail'] += 1


